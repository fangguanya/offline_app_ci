class IvrStyle {
  static TextStyle getWarningTextStyle(bool b) {
    if(!b) {
      return TextStyle(color: Colors.red);
    } else {
      return TextStyle(color: Colors.green);
    }
  }
  static TextStyle get warningStyle {
    return TextStyle(color: Colors.orange);
  }

  static TextStyle getDeviceStateTextStyle(DeviceStateValue v) {
    if(v == DeviceStateValue.OFF) {
      return TextStyle(color: Colors.red);
    } else if(v == DeviceStateValue.CONN) {
      return TextStyle(color: Colors.orange);
    } else if(v == DeviceStateValue.RUN) {
      return TextStyle(color: Colors.green);
    } else {
      return TextStyle(color: Colors.black);
    }
  }

  static TextStyle getFont48({color}) {
    return TextStyle(fontFamily: "PingFang", fontSize: Adapt.px(48), color:color, decoration: TextDecoration.none);
  }
  static TextStyle getFont40({color}) {
    return TextStyle(fontFamily: "PingFang", fontSize: Adapt.px(40), color:color, decoration: TextDecoration.none);
  }
  static TextStyle getFont44({color}) {
    return TextStyle(fontFamily: "PingFang", fontSize: Adapt.px(44), color:color, decoration: TextDecoration.none);
  }
  static TextStyle getFont52({color}) {
    return TextStyle(fontFamily: "PingFang", fontSize: Adapt.px(52), color:color, decoration: TextDecoration.none);
  }
  static TextStyle getFont36({color}) {
    return TextStyle(fontFamily: "PingFang", fontSize: Adapt.px(36), color:color, decoration: TextDecoration.none);
  }
  static TextStyle getFont30({color}) {
    return TextStyle(fontFamily: "PingFang", fontSize: Adapt.px(30), color:color, decoration: TextDecoration.none);
  }
